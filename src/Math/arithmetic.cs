using System;
class arithmetic
{
    public static void add(double firstNumber, double secondNumber)
    {
        double sum;
        
        sum = firstNumber + secondNumber;

        return sum;
    }
    public static void subtract(double firstNumber, double secondNumber)
    {
        double sum;
        
        sum = firstNumber - secondNumber;

        return sum;
    }
    public static void multiply(double firstNumber, double secondNumber)
    {
        double sum;
        
        sum = firstNumber * secondNumber;

        return sum;
    }
    public static void division(double firstNumber, double secondNumber)
    {
        double sum;
        
        sum = firstNumber / secondNumber;

        return sum;
    }
    public static void modulo(double firstNumber, double secondNumber)
    {
        double rest;
        
        rest = firstNumber % secondNumber;

        return rest;
    }
}
